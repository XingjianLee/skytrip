# 创建旅行社表，存储旅行社信息
CREATE TABLE agencies (
    agency_id BIGINT AUTO_INCREMENT PRIMARY KEY,
    agency_name VARCHAR(100) NOT NULL COMMENT '旅行社全称',
    business_license VARCHAR(50) UNIQUE NOT NULL COMMENT '营业执照注册号',
    contact_phone VARCHAR(20),
    address VARCHAR(255)
);

